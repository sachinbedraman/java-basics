package org.example.java.basics;

public class DataTypes {

    public static void main(String[] args) {

        // Primitive types integers
        byte b = 1; // -127 to +128 8 bit
        short short_type = 23; // 16 bit
        int int_type = 23232; // 32 bit
        long long_type = 232323232; // 64 bit

        System.out.println(b);
        System.out.println(short_type);
        System.out.println(int_type);
        System.out.println(long_type);

        // Decimals
        float float_type = 23.23f; // 32 bit
        double double_type = 43232.45; // 64 bit
        System.out.println(float_type);
        System.out.println(double_type);

        // Characters
        char char_type = 'd';
    }
}
